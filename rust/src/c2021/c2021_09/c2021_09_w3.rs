/// Minimize Max Distance to Gas Station
/// ====================================
///
/// You are given an integer array `stations` that represents the positions of the gas stations on the __x-axis__.
/// You are also given an integer `k`.
///
/// You should add `k` new gas stations.
/// You can add the stations anywhere on the __x-axis__, and not necessarily on an integer position.
///
/// Let `penalty()` be the maximum distance between __adjacent__ gas stations after adding the `k` new stations.
///
/// Return _the smallest possible value of `penalty()`_.
/// Answers within `0.00001` of the actual answer will be accepted.
///
/// __Constraints:__
///
/// - `10 <= stations.length <= 2000`
/// - `0 <= stations[i] <= 100_000_000`
/// - `stations` is sorted in a __strictly increasing__ order.
/// - `1 <= k <= 1_000_000`
///
/// https://leetcode.com/explore/challenge/card/september-leetcoding-challenge-2021/638/week-3-september-15th-september-21st/3975/
struct Solution;
impl Solution {
    pub fn minmax_gas_dist(stations: Vec<i32>, k: i32) -> f64 {
        use std::cmp;

        #[derive(PartialEq, PartialOrd, Debug, Copy, Clone)]
        struct D(f64);
        impl Eq for D {}
        impl Ord for D {
            fn cmp(&self, other: &D) -> cmp::Ordering {
                self.partial_cmp(other).unwrap()
            }
        }

        fn can_get(e: f64, mut k: i32, dists: &[f64]) -> bool {
            for &d in dists {
                if d <= e && k >= 0 {
                    return true;
                }
                if k <= 0 {
                    return false;
                }
                k -= (d / e) as i32;
            }
            k >= 0
        }

        let mut dists = Vec::with_capacity(stations.len() - 1);
        for w in stations.windows(2) {
            let (a, b) = (w[0], w[1]);
            dists.push((b - a) as f64);
        }
        dists.sort_unstable_by(|a, b| b.partial_cmp(a).unwrap());
        let mut l = dists[dists.len() - 1] / k as f64;
        let mut r = dists[0];
        while (r - l) > 0.00001 {
            let m = l + (r - l) / 2.0;
            if can_get(m, k, &dists) {
                r = m;
            } else {
                l = m;
            }
        }
        l
    }

    pub fn minmax_gas_dist_time_limit_exceeded(stations: Vec<i32>, mut k: i32) -> f64 {
        use std::cmp;
        use std::collections::BinaryHeap;

        #[derive(PartialEq, PartialOrd, Debug, Copy, Clone)]
        struct D(f64);
        impl Eq for D {}
        impl Ord for D {
            fn cmp(&self, other: &D) -> cmp::Ordering {
                self.partial_cmp(other).unwrap()
            }
        }

        let mut max_dists = BinaryHeap::with_capacity(stations.len() + k as usize);
        for w in stations.windows(2) {
            let (a, b) = (w[0], w[1]);
            let d = D((b - a) as f64);
            max_dists.push((d, d, D(1.0)));
        }
        while k > 0 {
            let (D(pd), D(i), D(c)) = max_dists.pop().unwrap();
            let c = c + 1.0;
            let d = i / c;
            max_dists.push((D(d), D(i), D(c)));
            if pd - d <= 0.00001 {
                k = 0;
            } else {
                k -= 1
            }
        }
        (max_dists.pop().unwrap().0).0
    }
}

